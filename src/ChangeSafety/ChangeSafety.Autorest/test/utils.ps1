# Add script method called AddWithCache to $env, when useCache is set true, it will try to get the value from the $env first.
# example: $val = $env.AddWithCache('key', $val, $true)
$env | Add-Member -Type ScriptMethod -Value { param( [string]$key, [object]$val, [bool]$useCache) if ($this.Contains($key) -and $useCache) { return $this[$key] } else { $this[$key] = $val; return $val } } -Name 'AddWithCache'
function setupEnv() {
    # Preload subscriptionId and tenant from context, which will be used in test
    # as default. You could change them if needed.
    $env.SubscriptionId = (Get-AzContext).Subscription.Id
    $env.Tenant = (Get-AzContext).Tenant.Id
    
    # Resource group and location for tests
    $env.ResourceGroupName = "rg-changesafety-test"
    $env.Location = "eastus"
    
    # Resource names for tests
    $env.StageMapName = "test-stagemap-$(RandomString $false 6)"
    $env.StageMapName2 = "test-stagemap2-$(RandomString $false 6)"
    $env.StageMapNameNested = "test-stagemap-nested-$(RandomString $false 6)"
    $env.ChangeRecordName = "test-changerecord-$(RandomString $false 6)"
    $env.ChangeRecordName2 = "test-changerecord2-$(RandomString $false 6)"
    $env.StageProgressionName = "test-stageprogression-$(RandomString $false 6)"
    
    # Ensure resource group exists for live tests
    if ($TestMode -eq 'live') {
        $rg = Get-AzResourceGroup -Name $env.ResourceGroupName -ErrorAction SilentlyContinue
        if (-not $rg) {
            New-AzResourceGroup -Name $env.ResourceGroupName -Location $env.Location
        }
    }

    $envFile = 'env.json'
    if ($TestMode -eq 'live') {
        $envFile = 'localEnv.json'
    }
    set-content -Path (Join-Path $PSScriptRoot $envFile) -Value (ConvertTo-Json $env)
}
