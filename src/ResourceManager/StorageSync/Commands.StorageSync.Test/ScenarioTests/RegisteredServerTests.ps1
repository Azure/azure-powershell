Test RegisteredServer
.DESCRIPTION
SmokeTest
#>
function Test-RegisteredServer
{
    # Setup
    $resourceGroupName = Get-StorageManagementTestResourceName;

    try
    {
        # Test
            $storageSyncServiceName = Get-StorageManagementTestResourceName
            $resourceLocation = "westcentralus"#Get-ProviderLocation ResourceManagement;

            Write-Verbose "RGName: $resourceGroupName | Loc: $resourceLocation"
            New-AzureRmResourceGroup -Name $resourceGroupName -Location $resourceLocation;

            Write-Verbose "Resource: $storageSyncServiceName | Loc: $resourceLocation"
            $storageSyncService = New-AzureRmStorageSyncService -ResourceGroupName $resourceGroupName -Location $resourceLocation -StorageSyncServiceName $storageSyncServiceName

            Write-Verbose "Resource: <auto-generated> | Loc: $resourceLocation"
            $job = Register-AzureRmStorageSyncServer -ResourceGroupName $resourceGroupName -StorageSyncServiceName $storageSyncServiceName -AsJob 
            $job | Wait-Job
            $expectedRegisteredServer = get-job -Id $job.Id | receive-job -Keep
            $expectedRegisteredServer

            Write-Verbose "Get RegisteredServer by ServerId"
            $registeredServer = Get-AzureRmStorageSyncServer -ResourceGroupName $resourceGroupName -StorageSyncServiceName $storageSyncServiceName -ServerId $expectedRegisteredServer.ServerId -Verbose
            Write-Verbose "Validating RegisteredServer Properties"
            Assert-AreEqual $registeredServer.ServerId $expectedRegisteredServer.ServerId

            Write-Verbose "Get RegisteredServer by ParentObject"
            $registeredServers = Get-AzureRmStorageSyncServer -ParentObject $storageSyncService -Verbose
            Assert-AreEqual $registeredServers.Length 1
            $registeredServer = $registeredServers[0]
            Write-Verbose "Validating RegisteredServer Properties"
            Assert-AreEqual $registeredServer.ServerId $expectedRegisteredServer.ServerId

            Write-Verbose "Get RegisteredServer by ParentResourceId"
            $registeredServers = Get-AzureRmStorageSyncServer -ParentResourceId $storageSyncService.ResourceId -Verbose
            Assert-AreEqual $registeredServers.Length 1
            $registeredServer = $registeredServers[0]
            Write-Verbose "Validating RegisteredServer Properties"
            Assert-AreEqual $registeredServer.ServerId $expectedRegisteredServer.ServerId

            Write-Verbose "Unregister Server: $($expectedRegisteredServer.ServerId)"
            Unregister-AzureRmStorageSyncServer -Force -ResourceGroupName $resourceGroupName -StorageSyncServiceName $storageSyncServiceName -ServerId $expectedRegisteredServer.ServerId -AsJob | Wait-Job

            Write-Verbose "Testing Piping scenarios"
            Register-AzureRmStorageSyncServer -ParentObject $storageSyncService | Get-AzureRmStorageSyncServer | Unregister-AzureRmStorageSyncServer -Force -AsJob | Wait-Job

            Register-AzureRmStorageSyncServer -ParentResourceId $storageSyncService.ResourceId | Unregister-AzureRmStorageSyncServer -Force -AsJob | Wait-Job

            Write-Verbose "Removing StorageSyncService: $storageSyncServiceName"
            Remove-AzureRmStorageSyncService -Force -ResourceGroupName $resourceGroupName -Name $storageSyncServiceName -AsJob | Wait-Job

    }
    finally
    {
        # Cleanup
        Write-Verbose "Removing ResourceGroup : $resourceGroupName"
        Clean-ResourceGroup $resourceGroupName
    }
}

